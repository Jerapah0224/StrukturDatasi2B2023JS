let kondisi = false;

if (kondisi) {
    console.log("Konsep Increment:");
    for (let i = 0; i <= 3; i++) {
        console.log(`Baris ke-${i}: Nilai i = ${i}`);
    };
} else {
    console.log("Konsep Decrement:");
    for (let i = 3; i > 0; i--) {
        console.log(`Baris ke-${4 - i}: Nilai i = ${i}`);
    };
};
